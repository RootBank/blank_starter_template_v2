#!/usr/bin/env node
/**
 * Regenerate llms-full.txt at the repo root by concatenating CLAUDE.md, AGENTS.md,
 * every numbered doc, the glossary, the Stripe reference, the spec template, and
 * the provider-integration prompt. Each file is preceded by an anchor of the form
 * `## <basename-without-extension>` so an agent can cite individual sections.
 *
 * Run via `npm run build:llms` (from collection_module/) or `node scripts/build-llms-full.js`.
 * Output is deterministic — running twice produces zero diff.
 */

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..');
const DOCS = path.join(REPO_ROOT, 'collection_module', 'docs');

const ORDER = [
  path.join(REPO_ROOT, 'CLAUDE.md'),
  path.join(REPO_ROOT, 'AGENTS.md'),
  path.join(DOCS, '00-OVERVIEW.md'),
  path.join(DOCS, '01-GETTING-STARTED.md'),
  path.join(DOCS, '02-ARCHITECTURE.md'),
  path.join(DOCS, '03-PROVIDER-INTERFACE.md'),
  path.join(DOCS, '04-CONTROLLERS.md'),
  path.join(DOCS, '05-CLIENTS.md'),
  path.join(DOCS, '06-ADAPTERS.md'),
  path.join(DOCS, '07-LIFECYCLE-HOOKS.md'),
  path.join(DOCS, '08-WEBHOOKS.md'),
  path.join(DOCS, '09-TESTING.md'),
  path.join(DOCS, '10-DEPLOYMENT.md'),
  path.join(DOCS, '11-CONFIGURATION.md'),
  path.join(DOCS, '12-ERROR-HANDLING.md'),
  path.join(DOCS, '13-BUILD-FROM-SPEC.md'),
  path.join(DOCS, '14-SELF-REVIEW.md'),
  path.join(DOCS, '15-PROVIDER-PATTERNS.md'),
  path.join(DOCS, '16-WHEN-TO-DEVIATE.md'),
  path.join(DOCS, 'GLOSSARY.md'),
  path.join(DOCS, 'STRIPE-REFERENCE.md'),
  path.join(DOCS, 'SPEC-TEMPLATE.md'),
  path.join(DOCS, 'PROVIDER-INTEGRATION-PROMPT.md'),
];

const HEADER =
  '<!-- Generated by scripts/build-llms-full.js. Do not edit by hand. -->\n' +
  '<!-- To regenerate: `npm run build:llms` from collection_module/ -->\n\n';

function anchor(filePath) {
  return path.basename(filePath, '.md');
}

const missing = ORDER.filter((p) => !fs.existsSync(p));
if (missing.length) {
  console.error('Missing input files:');
  for (const m of missing) console.error('  ' + path.relative(REPO_ROOT, m));
  process.exit(1);
}

const parts = [HEADER];
for (const file of ORDER) {
  const body = fs.readFileSync(file, 'utf8').replace(/\s+$/, '');
  parts.push(`## ${anchor(file)}\n\n${body}\n\n`);
}

const output = parts.join('');
const outPath = path.join(REPO_ROOT, 'llms-full.txt');
fs.writeFileSync(outPath, output);
console.log(`wrote ${path.relative(REPO_ROOT, outPath)} (${output.length} bytes, ${ORDER.length} sources)`);
